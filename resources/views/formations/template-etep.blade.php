                          <h2 class="heading">Le seul diplôme de master en informatique, en ligne conçu pour les étudiants sans formation en informatique.</h2>
                          <div class="rc-Markdown styled">
                            <p>
                              <!-- react-text: 212 -->Le diplôme en ligne de maîtrise en informatique et en technologies de l'information (MCIT) est un programme d'études de premier cycle en informatique, unique en son genre, conçu pour les majors non-informaticiens. Proposé par l'Université de Pennsylvanie, une institution de l'Ivy League, ce nouveau programme met en ligne le diplôme MCIT de longue date et bien établi sur le campus. Le programme MCIT permet aux étudiants sans formation en informatique de réussir dans les domaines de l'informatique et de la technologie. Les étudiants du MCIT proviennent de divers milieux académiques allant de la littérature à l'histoire en passant par la chimie et la médecine.
                              <!-- /react-text -->
                            </p>
                            <p>
                              <!-- react-text: 214 -->L'informatique révolutionne la société mondiale et crée de nouveaux domaines, industries et carrières qui n'avaient jamais été entendues il y a à peine dix, voire cinq ans. Les diplômés du MCIT seront particulièrement bien placés pour occuper des postes techniques dans des secteurs tels que la bioinformatique, la médecine, la finance et les télécommunications, ainsi que dans le secteur du développement de logiciels de base. L'exposition à des projets concrets tout au long du programme préparera les étudiants à utiliser leurs compétences pour avoir un impact positif sur la société.
                              <!-- /react-text -->
                            </p>
                            <p>
                              <!-- react-text: 216 -->Ce programme favorise une communauté riche et inclusive pour les étudiants, qui auront accès aux services de base de toute l'université pour soutenir leur bien-être et leurs objectifs de carrière futurs. Ce soutien, associé à un réseau solide d’anciens élèves et à un département Career Services, garantit que chaque étudiant est prêt à réussir, même s'il est nouveau dans le secteur des technologies. À la fin de leurs études, les anciens du MCIT ont décroché des emplois rémunérateurs dans des entreprises technologiques telles que Amazon, Facebook, Google, Microsoft, Oracle et Bloomberg.
                              <!-- /react-text -->
                            </p>
                            <h2>
                              <!-- react-text: 218 -->Qu'est-ce qui rend ce diplôme unique?
                              <!-- /react-text -->
                            </h2>
                            <ul>
                              <li><strong>
                                  <!-- react-text: 222 -->Qualité de l’Ivy League
                                  <!-- /react-text --></strong><br><!-- react-text: 224 -->C’est un programme unique en son genre qui offre un diplôme de maîtrise en informatique de l’Ivy League conçu pour les non-majors.
                                <!-- /react-text -->
                              </li>
                              <li><strong>
                                  <!-- react-text: 227 -->Construit autour de votre emploi du temps
                                  <!-- /react-text --></strong><br><!-- react-text: 229 -->100% en ligne. Vous bénéficierez du même enseignement de haute qualité que les étudiants sur le campus et obtiendrez votre diplôme avec le même diplôme. Le diplôme n'indique pas si le diplôme a été obtenu en ligne ou sur le campus.
                                <!-- /react-text -->
                              </li>
                              <li><strong>
                                  <!-- react-text: 232 -->Prix ​​abordable
                                  <!-- /react-text --></strong><br><!-- react-text: 234 -->À 26 300 $ pour les frais de scolarité et les frais, le diplôme en ligne MCIT coûte nettement moins cher que les alternatives sur le campus et la plupart des maîtrises en ligne en informatique.
                                <!-- /react-text -->
                              </li>
                              <li><strong>
                                  <!-- react-text: 237 -->Accès à la Faculté
                                  <!-- /react-text --></strong><br><!-- react-text: 239 -->Vous aurez un accès fréquent à des instructeurs de renommée mondiale via des forums de discussion et des heures de bureau en direct.
